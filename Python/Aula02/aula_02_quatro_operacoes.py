# 1) Ler dois números inteiros do teclado e imprimir sua soma, multiplicação, divisão e subtração.

num_a = int(input("Valor A: "))
num_b = int(input("Valor B: "))

print("Soma = {}".format(num_a + num_b))
print("Multiplicação = {}".format(num_a * num_b))
print("Divisão = {}".format(num_a / num_b))
print("Subtração = {}".format(num_a - num_b))
